//A C program that uses a for loops
#include<stdio.h>

int main() {

    int i;

    for(i = 0; i < 10; i++) {
        printf("Hello!\n");

    }

    int j;

    for(j = 0; j < 10; j++) {
        printf("%d", j);

    }

    int k;

    for(k = 0; k < 10; k++) {
        printf("%d\n", k);

    }

    return 0;

}
